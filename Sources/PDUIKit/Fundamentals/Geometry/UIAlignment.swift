
/// An enumeration of alignment options along a horizontal axis.
public enum UIHorizontalAlignment {
    /// The left or leading side.
    case leading

    /// The horizontal center.
    case center

    /// The right or trailing side.
    case trailing
}

/// An enumeration of the alignment options along a vertical axis.
public enum UIVerticalAlignment {
    /// The top side.
    case top

    /// The vertical center.
    case center

    /// The bottom side.
    case bottom
}
